                <p><b>What's TEDx?</b></p>
                <p>Under the motto "ideas worth spreading," TED Conferences, LLC, an American-Canadian non-profit media organization, uploads international presentations online for free distribution.</p>

                <p>A TEDx event is a community gathering where live acts and TED-like speeches are shared. Each TEDx community plans and manages its events autonomously.</p>

                <p><b>About TEDxIIMKashipur</b></p>

                <p>We are a team of student volunteers who put together a one-of-a-kind event. We aim to bring together thinkers, specialists, decision-makers, and change agents who alter mindsets and bring about systematic change in people's lives. Every year, a weekend of carefully curated Talks that spark conversation in our community and beyond.&nbsp;&nbsp;</p>

                <p>For the past six years, we have organized our flagship Annual TEDxIIMKashipur Conference, sharing with our community the insights and experiences of corporate executives, academicians, engineers, politicians, and many others.&nbsp;</p>

                <p>Our Talks are published on the popular TEDx YouTube channel for free for national and international audiences. Additionally, we provide access to the Talks on this website too.</p>

                <p><b>The theme for 2022: Time and Time Again!</b></p>

                <p>TEDxIIMkashipur is back with another thought-provoking edition of TED,&nbsp;<strong>Time and Time Again</strong>.</p>

                <p><em><b>Time and time again</b></em>&nbsp;refers to something that occurs quite often; it could reflect experiences, moments, etc. Like history often repeats itself, a moment can neither buried nor shunned. It repeats itself and comes back to us. To this line, we bring you yet another exuberant lineup of exuberant speakers to share their excerpts with us!</p>
                                  

